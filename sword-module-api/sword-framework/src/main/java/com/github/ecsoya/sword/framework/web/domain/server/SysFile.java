package com.github.ecsoya.sword.framework.web.domain.server;

/**
 * The Class SysFile.
 */
public class SysFile {

	/** The dir name. */
	private String dirName;

	/** The sys type name. */
	private String sysTypeName;

	/** The type name. */
	private String typeName;

	/** The total. */
	private String total;

	/** The free. */
	private String free;

	/** The used. */
	private String used;

	/** The usage. */
	private double usage;

	/**
	 * Gets the dir name.
	 *
	 * @return the dir name
	 */
	public String getDirName() {
		return dirName;
	}

	/**
	 * Sets the dir name.
	 *
	 * @param dirName the new dir name
	 */
	public void setDirName(String dirName) {
		this.dirName = dirName;
	}

	/**
	 * Gets the sys type name.
	 *
	 * @return the sys type name
	 */
	public String getSysTypeName() {
		return sysTypeName;
	}

	/**
	 * Sets the sys type name.
	 *
	 * @param sysTypeName the new sys type name
	 */
	public void setSysTypeName(String sysTypeName) {
		this.sysTypeName = sysTypeName;
	}

	/**
	 * Gets the type name.
	 *
	 * @return the type name
	 */
	public String getTypeName() {
		return typeName;
	}

	/**
	 * Sets the type name.
	 *
	 * @param typeName the new type name
	 */
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	/**
	 * Gets the total.
	 *
	 * @return the total
	 */
	public String getTotal() {
		return total;
	}

	/**
	 * Sets the total.
	 *
	 * @param total the new total
	 */
	public void setTotal(String total) {
		this.total = total;
	}

	/**
	 * Gets the free.
	 *
	 * @return the free
	 */
	public String getFree() {
		return free;
	}

	/**
	 * Sets the free.
	 *
	 * @param free the new free
	 */
	public void setFree(String free) {
		this.free = free;
	}

	/**
	 * Gets the used.
	 *
	 * @return the used
	 */
	public String getUsed() {
		return used;
	}

	/**
	 * Sets the used.
	 *
	 * @param used the new used
	 */
	public void setUsed(String used) {
		this.used = used;
	}

	/**
	 * Gets the usage.
	 *
	 * @return the usage
	 */
	public double getUsage() {
		return usage;
	}

	/**
	 * Sets the usage.
	 *
	 * @param usage the new usage
	 */
	public void setUsage(double usage) {
		this.usage = usage;
	}
}
