package com.github.ecsoya.sword.framework.web.domain.server;

/**
 * The Class Sys.
 */
public class Sys {

	/** The computer name. */
	private String computerName;

	/** The computer ip. */
	private String computerIp;

	/** The user dir. */
	private String userDir;

	/** The os name. */
	private String osName;

	/** The os arch. */
	private String osArch;

	/**
	 * Gets the computer name.
	 *
	 * @return the computer name
	 */
	public String getComputerName() {
		return computerName;
	}

	/**
	 * Sets the computer name.
	 *
	 * @param computerName the new computer name
	 */
	public void setComputerName(String computerName) {
		this.computerName = computerName;
	}

	/**
	 * Gets the computer ip.
	 *
	 * @return the computer ip
	 */
	public String getComputerIp() {
		return computerIp;
	}

	/**
	 * Sets the computer ip.
	 *
	 * @param computerIp the new computer ip
	 */
	public void setComputerIp(String computerIp) {
		this.computerIp = computerIp;
	}

	/**
	 * Gets the user dir.
	 *
	 * @return the user dir
	 */
	public String getUserDir() {
		return userDir;
	}

	/**
	 * Sets the user dir.
	 *
	 * @param userDir the new user dir
	 */
	public void setUserDir(String userDir) {
		this.userDir = userDir;
	}

	/**
	 * Gets the os name.
	 *
	 * @return the os name
	 */
	public String getOsName() {
		return osName;
	}

	/**
	 * Sets the os name.
	 *
	 * @param osName the new os name
	 */
	public void setOsName(String osName) {
		this.osName = osName;
	}

	/**
	 * Gets the os arch.
	 *
	 * @return the os arch
	 */
	public String getOsArch() {
		return osArch;
	}

	/**
	 * Sets the os arch.
	 *
	 * @param osArch the new os arch
	 */
	public void setOsArch(String osArch) {
		this.osArch = osArch;
	}
}
