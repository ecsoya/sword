package com.soyatec.sword.report.domain;

import java.util.Date;

public class DateCount {

	private Date date;

	private Long count;

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public Long getCount() {
		return count;
	}

	public void setCount(Long count) {
		this.count = count;
	}

}
